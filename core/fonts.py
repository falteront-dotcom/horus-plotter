@functools.lru_cache(maxsize=16)
def resolve_font(size_px: int):
    for name in _FONT_CHAIN:
        try:
            return ImageFont.truetype(name, size_px)
        except OSError:
            continue
    return ImageFont.load_default()

Stroke = List[Tuple[float, float]]
Dot = Tuple[float, float]
CharDef = Tuple[List[Stroke], float, List[Dot]]

_m = lambda *args: list(args)
_el = lambda cx, cy, rx, ry, s=0, e=360, n=18: [
    (cx + math.cos(math.radians(s + (e-s)*i/n)) * rx,
     cy + math.sin(math.radians(s + (e-s)*i/n)) * ry)
    for i in range(n+1)
]

