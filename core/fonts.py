def _build_font(lc_func) -> Dict[str, CharDef]:
    """Build a full font mapping from lowercase function + uppercase scaling."""
    lc = lc_func()
    result: Dict[str, CharDef] = {}
    
    # Lowercase
    for ch, defn in lc.items():
        result[ch] = defn
    
    # Uppercase (scaled 1.13x from lowercase)
    ru_lower = 'абвгдеёжзийклмнопрстуфхцчшщъыьэюя'
    for low in ru_lower:
        up = low.upper()
        if low in lc:
            strokes, w, dots = lc[low]
            scaled = [[(x * 1.05, y * 1.18 + .02) for x, y in s] for s in strokes]
            scaled_dots = [(x, y * 1.08 + .06) for x, y in dots]
            result[up] = (scaled, w * 1.06, scaled_dots)
    
    # Digits
    result.update(_DIGITS)
    
    # Common symbols
    result.update(_COMMON_SYMBOLS)
    
    # Space
    result[' '] = _SPACE
    result['\n'] = _SPACE
    result['\t'] = _SPACE
    
    return result

