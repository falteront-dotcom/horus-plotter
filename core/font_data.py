# Build the full mapping
FONT_MAP: Dict[str, CharDef] = {}

# Add lowercase
for ch, defn in _LC.items():
    FONT_MAP[ch] = defn

# Add uppercase
for ch, defn in _UC.items():
    FONT_MAP[ch] = defn

# Add Latin digits
for ch, defn in _LATIN_DIGITS.items():
    FONT_MAP[ch] = defn

# Add Latin letters (mapped to Cyrillic)
for lat, src in _LATIN_MAP.items():
    FONT_MAP[lat] = FONT_MAP[src]
    FONT_MAP[lat.upper()] = FONT_MAP[src.upper()]

# Space
FONT_MAP[' '] = _SPACE
FONT_MAP['\n'] = _SPACE
FONT_MAP['\t'] = _SPACE

# Default for unknown chars
_DEFAULT: CharDef = ([_make((.10, .05), (.50, .90))], .40, [])


def get_char_definition(ch: str) -> CharDef:
    """Return the stroke definition for a character, or default if unknown."""
    return FONT_MAP.get(ch, _DEFAULT)

def supported_chars() -> str:
    """Return a string of all supported characters."""
    return ''.join(sorted(FONT_MAP.keys()))
