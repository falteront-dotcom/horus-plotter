# Build uppercase by scaling lowercase
_UC: Dict[str, CharDef] = {}
for ch in list('абвгдеёжзийклмнопрстуфхцчшщъыьэюя'):
    strokes, w, dots = _LC[ch]
    scaled_strokes = [[(x * 1.05, y * 1.18 + .02) for x, y in s] for s in strokes]
    scaled_dots = [(x, y * 1.08 + .06) for x, y in dots]
    _UC[ch.upper()] = (scaled_strokes, w * 1.06, scaled_dots)

