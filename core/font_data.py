"""
Handwriting font stroke data — Semyon's personal cursive.

Each character is defined in 0-1 coordinate space:
  - strokes: list of polylines [(x, y), ...] 
  - width: character advance width (in 0-1 space)
  - dots: single points for punctuation (i, !, ? dots et al.)

To render at line_height_mm, multiply all coords by line_height_mm.
Character advance = width * line_height_mm.
"""

